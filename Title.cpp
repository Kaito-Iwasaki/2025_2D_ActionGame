//=====================================================================
// 初期化処理
//=====================================================================
void InitTitle(void)
{
	InitDecal();

	SetDecal(
		DECAL_LABEL_TITLE_BG000,
		D3DXVECTOR3(SCREEN_CENTER, SCREEN_VCENTER, 0),
		D3DXVECTOR3(SCREEN_WIDTH, SCREEN_HEIGHT, 0),
		D3DXVECTOR3_ZERO,
		D3DXCOLOR(1.0f, 1.0f, 1.0f, 1.0f)
	);
}

//=====================================================================
// 終了処理
//=====================================================================
void UninitTitle(void)
{
	UninitDecal();
}

//=====================================================================
// 更新処理
//=====================================================================
void UpdateTitle(void)
{
	if (INPUT_TRIGGER_ACCEPT)
	{// ゲームスタート
		SetStage(0);
		SetFade(SCENE_GAME);
	}
}

//=====================================================================
// 描画処理
//=====================================================================
void DrawTitle(void)
{
	DrawDecal();
}