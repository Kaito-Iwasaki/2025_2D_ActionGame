//=====================================================================
// 初期化処理
//=====================================================================
void InitGame(void)
{
	InitPlayer();
	InitBlock();

	SetBlock(
		BLOCK_TYPE_000,
		D3DXVECTOR3(SCREEN_CENTER + 200, 600, 0)
	);

	SetBlock(
		BLOCK_TYPE_000,
		D3DXVECTOR3(SCREEN_CENTER + 100, 500, 0)
	);

	SetBlock(
		BLOCK_TYPE_000,
		D3DXVECTOR3(SCREEN_CENTER, 400, 0)
	);

	SetBlock(
		BLOCK_TYPE_000,
		D3DXVECTOR3(SCREEN_CENTER - 100, 500, 0)
	);

	SetBlock(
		BLOCK_TYPE_000,
		D3DXVECTOR3(SCREEN_CENTER - 200, 600, 0)
	);

	for (int i = 0; i < SCREEN_WIDTH / 48; i++)
	{
		SetBlock(
			BLOCK_TYPE_000,
			D3DXVECTOR3(i * 48, SCREEN_HEIGHT, 0)
		);
	}
	for (int i = 0; i < SCREEN_HEIGHT / 48; i++)
	{
		SetBlock(
			BLOCK_TYPE_000,
			D3DXVECTOR3(0 - 48 , SCREEN_HEIGHT - 48 * (i + 1), 0)
		);
	}
	for (int i = 0; i < SCREEN_HEIGHT / 48; i++)
	{
		SetBlock(
			BLOCK_TYPE_000,
			D3DXVECTOR3(SCREEN_WIDTH, SCREEN_HEIGHT - 48 * (i + 1), 0)
		);
	}
}

//=====================================================================
// 終了処理
//=====================================================================
void UninitGame(void)
{
	UninitPlayer();
	UninitBlock();
}

//=====================================================================
// 更新処理
//=====================================================================
void UpdateGame(void)
{
	UpdatePlayer();
	UpdateBlock();
}

//=====================================================================
// 描画処理
//=====================================================================
void DrawGame(void)
{
	DrawBlock();
	DrawPlayer();
}